/**
 * Базовый тип для всех текстовых узлов.
 */
export type NodeText = {
  /** Тип узла */
  type: "text"
  /** Исходный текст (опционально) */
  text?: string
  /** Путь к данным (опционально) */
  data?: string | string[]
  /** Значение (опционально) */
  value?: string
  /** Выражение (опционально) */
  expr?: string
}
